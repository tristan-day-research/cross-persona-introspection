"""Versioned prompt WORDING for the self-recognition cases.

Case STRUCTURE — persona roles, counterbalancing, answer keys, enumeration — lives
in evaluation_cases.py and is shared across all wording versions. Only the verbatim
wording of each case (its ``intro`` and ``option_sentences``) is versioned here, so
the question can be reworded while older runs stay reproducible by pinning a version.

  "v1" — the ORIGINAL framing, e.g. "...generated under your current persona prompt".
         This is the wording baked into CASE_REGISTRY in evaluation_cases.py, kept
         verbatim as the reference for prior experiments. v1 therefore has NO
         overrides here — resolve_spec uses the registry values directly.

  "v2" — natural, direct first-person framing, following the two papers this
         experiment is based on (Panickssery et al. 2024, "which summary did you
         write?"; Ackerman & Panickssery 2025, "who do you think wrote this?" with
         options "Me" / "I wrote it"). The persona is asked about ITSELF rather than
         about "which configuration generated a text". Only the active-self cases
         (1, 2, 3, 4, 7, 11) are reworded; the neutral third-party cases
         (5, 6, 8, 9, 10) keep their v1 label wording (the persona is not being asked
         about itself there), so they are OMITTED below and fall back to v1.

To add a version: append it to PROMPT_VERSIONS and add a dict of per-case overrides
to WORDING_OVERRIDES. Each override entry may set "intro" and/or "option_sentences";
the keys of "option_sentences" must match the case's CaseSpec.answer_keys().
"""

from __future__ import annotations

PROMPT_VERSIONS = ("v1", "v2")
DEFAULT_PROMPT_VERSION = "v1"


# ── v2: natural first-person wording for the active-self cases ────────────────
# option_sentences keys mirror the structural answer_keys in evaluation_cases.py:
#   single-text  → "current" / "other"
#   paired single→ "text1" / "text2"
#   attribution  → "t1cur_t2oth" / "t1oth_t2cur"

_V2_WHICH_TEXT_SELF = {  # paired "which text is yours" (cases 3, 7, 11)
    "text1": "I wrote Text 1.",
    "text2": "I wrote Text 2.",
}

_V2: dict[str, dict] = {
    "case1": {
        "intro": ("Below is a piece of text that was generated by an AI assistant in "
                  "response to a task. It may or may not have been written by you."),
        "option_sentences": {
            "current": "I wrote this text.",
            "other": "I did not write this text.",
        },
    },
    "case2": {
        "intro": ("Below is a piece of text that was generated by an AI assistant in "
                  "response to a task. It was written either by you or by the other "
                  "persona described below."),
        "option_sentences": {
            "current": "I wrote this text.",
            "other": "The other persona (described above) wrote this text.",
        },
    },
    "case3": {
        "intro": ("Below are two pieces of text generated by AI assistants in response "
                  "to the same task. You wrote one of them, and the other persona "
                  "described below wrote the other."),
        "option_sentences": _V2_WHICH_TEXT_SELF,
    },
    "case4": {
        "intro": ("Below are two pieces of text generated by AI assistants in response "
                  "to the same task. You wrote one of them, and the other persona "
                  "described below wrote the other. Attribute each text."),
        "option_sentences": {
            "t1cur_t2oth": "I wrote Text 1, and the other persona wrote Text 2.",
            "t1oth_t2cur": "The other persona wrote Text 1, and I wrote Text 2.",
        },
    },
    "case7": {
        "intro": ("Below are two pieces of text generated by AI assistants in response "
                  "to the same task. You wrote one of them, and a different persona "
                  "wrote the other."),
        "option_sentences": _V2_WHICH_TEXT_SELF,
    },
    "case11": {
        "intro": ("Below are two pieces of text generated by AI assistants in response "
                  "to the same task. You wrote one of them, and the other persona "
                  "described below wrote the other."),
        "option_sentences": _V2_WHICH_TEXT_SELF,
    },
}

# version → {case_id: {"intro"?: str, "option_sentences"?: dict}}.
# v1 is the registry itself (no overrides). A case missing from a version's dict
# falls back to its v1 (registry) wording.
WORDING_OVERRIDES: dict[str, dict[str, dict]] = {
    "v2": _V2,
}
