# Options
# ═══════════════════════════════════════════════════════════════════════════

@dataclass
class CrossModelOptions:
    eval_models: tuple[str, ...]
    generation_sources: dict[str, str]   # model_name → generations root path
    groups: tuple[str, ...]
    personas: tuple[str, ...]
    foil_types: tuple[str, ...] = FOIL_TYPES
    conditions: tuple[str, ...] = CONDITIONS
    max_pairs_per_cell: int = 60         # logical (foil_source, task) pairs per
    #   (eval_model, eval_persona, foil_type); each expands to 4 counterbalanced
    #   rows per condition.
    sampling_seed: int = 42
    save_logprobs: bool = True
    eval_dir: str = "cross_model_v1"
    dry_run: bool = False
    batch_size: int = 16


