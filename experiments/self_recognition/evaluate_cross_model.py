# Config parsing
# ═══════════════════════════════════════════════════════════════════════════

def _build_options(exp_config: dict) -> CrossModelOptions:
    eval_models = exp_config.get("eval_models")
    if not eval_models or len(eval_models) < 2:
        raise ValueError("cross_model eval needs `eval_models:` with >= 2 model names")
    eval_models = [str(m) for m in eval_models]

    sources = exp_config.get("generation_sources")
    if not isinstance(sources, dict):
        raise ValueError("cross_model eval needs `generation_sources:` mapping each "
                         "model name to its generations root path")
    missing_src = [m for m in eval_models if m not in sources]
    if missing_src:
        raise ValueError(f"generation_sources missing entries for {missing_src}")
    for m, p in sources.items():
        if not Path(p).is_dir():
            raise FileNotFoundError(f"generation_sources[{m!r}] not a directory: {p}")

    groups = exp_config.get("groups") or ["self_recognition_by_persona_category"]
    if isinstance(groups, str):
        groups = [groups]
    groups = tuple(str(g) for g in groups)

    personas = _resolve_personas(exp_config, eval_models, sources, groups)

    foil_types = exp_config.get("foil_types") or list(FOIL_TYPES)
    bad = [f for f in foil_types if f not in FOIL_TYPES]
    if bad:
        raise ValueError(f"unknown foil_types {bad}; valid: {list(FOIL_TYPES)}")
    conditions = exp_config.get("conditions") or list(CONDITIONS)
    bad = [c for c in conditions if c not in CONDITIONS]
    if bad:
        raise ValueError(f"unknown conditions {bad}; valid: {list(CONDITIONS)}")

    return CrossModelOptions(
        eval_models=tuple(eval_models),
        generation_sources={m: str(sources[m]) for m in eval_models},
        groups=groups,
        personas=tuple(personas),
        foil_types=tuple(foil_types),
        conditions=tuple(conditions),
        max_pairs_per_cell=int(exp_config.get("max_pairs_per_cell", 60)),
        sampling_seed=int(exp_config.get("sampling_seed", exp_config.get("seed", 42))),
        save_logprobs=bool(exp_config.get("save_logprobs", True)),
        eval_dir=str(exp_config.get("eval_dir") or exp_config.get("run_name") or "cross_model_v1"),
        dry_run=bool(exp_config.get("dry_run", False)),
        batch_size=int(exp_config.get("batch_size", 16)),
    )


def _resolve_personas(exp_config: dict, eval_models, sources, groups) -> list[str]:
    """The personas to use: explicit `personas:` (validated to exist for every
    model in every group), or — with `personas: ALL` / omitted — the INTERSECTION
    of personas present across all models' first group (so every pairing resolves)."""
    requested = exp_config.get("personas")
    present_per_model = []
    for m in eval_models:
        root = Path(sources[m])
        have: set[str] = set()
        for sub in root.iterdir() if root.is_dir() else []:
            if any((sub / g / "summaries.json").exists() for g in groups):
                have.add(sub.name)
        present_per_model.append(have)
    common = set.intersection(*present_per_model) if present_per_model else set()
    if not common:
        raise FileNotFoundError(
            f"no persona is present across all eval_models under {list(sources.values())} "
            f"for groups {list(groups)}")
    if requested in (None, "ALL", ["ALL"]):
        return sorted(common)
    requested = [str(p) for p in requested]
    missing = [p for p in requested if p not in common]
    if missing:
        raise ValueError(f"personas {missing} are not present across all eval_models "
                         f"(available in all: {sorted(common)})")
    return requested


# ═══════════════════════════════════════════════════════════════════════════
# Orchestration
