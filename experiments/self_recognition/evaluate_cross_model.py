"""Cross-model self-recognition evaluation.

A standalone eval (parallels evaluate_self_recognition.py's binary path) that
asks a *generalized* "which did you write?" question where the two candidate
texts can come from DIFFERENT models — not just different personas of one model.

The design separates two axes of "self":

  * MODEL identity  — is the text from my model (Llama) or the other (Qwen)?
  * PERSONA identity — is the text from my persona or a different one?

The evaluator is a (model, persona) pair; its SELF text is the one generated by
that same (evaluator_model, evaluator_persona). Each trial pairs the self text
against one FOIL on the same task, where the foil differs along one/both axes:

  diff_model_same_persona  — other model, same persona  → MODEL recognition
                             (e.g. Llama-neutral vs Qwen-neutral: your Q1)
  same_model_diff_persona  — same model, other persona  → within-model persona rec
  diff_model_diff_persona  — both differ                → easiest

Both conditions run: `active` (evaluator persona induced as system prompt) and
`neutral` (no system prompt — the baseline that isolates the active-state gain).
Text order and answer letter are counterbalanced, so chance = 0.5 and raw
accuracy is position/letter-bias free. The A/B answer is read with the same
ordering-bias-free constrained-logit reader as the paper phases
(core.self_recognition.choice_logprobs_batch).

Both models act as evaluators. Because one process holds one model, evaluator
models run SEQUENTIALLY; each writes under its own model-slugged eval dir
(results/text_evaluations/<task>/<model_slug>/<eval_dir>/), so the analysis
notebook loads Llama and Qwen exactly as it already loads per-model runs.

Config (mode: evaluate_cross_model). Two models + their generation source roots
are variables:

    eval_models:
      - meta-llama/Llama-3.1-8B-Instruct
      - Qwen/Qwen2.5-32B-Instruct
    generation_sources:
      meta-llama/Llama-3.1-8B-Instruct: results/text_generations/persona_category/Llama-3-1-8B-Instruct/personacat_v1
      Qwen/Qwen2.5-32B-Instruct:        results/text_generations/persona_category/Qwen2-5-32B-Instruct/personacat_v1

Run:
    python -m experiments.self_recognition.evaluate_cross_model <config>
    python run.py <config>                    # routed via mode: evaluate_cross_model
"""

