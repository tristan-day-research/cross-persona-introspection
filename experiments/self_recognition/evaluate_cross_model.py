def _dry_run_report(opts: CrossModelOptions, texts: dict, eval_model: str,
                    trials: list[CrossModelTrial], personas: dict[str, PersonaConfig]) -> None:
    from collections import Counter
    by = Counter(f"{t.base_case}|{t.foil_type}|{t.condition}" for t in trials)
    print("\n" + "=" * 72)
    print(f"CROSS-MODEL DRY RUN — evaluator = {eval_model}")
    print(f"{len(trials)} trials | personas={len(opts.personas)} | groups={list(opts.groups)}")
    print("=" * 72)
    for k, v in sorted(by.items()):
        print(f"  {k}: {v}")
    if trials:
        frac_A = sum(t.correct_answer == "A" for t in trials) / len(trials)
        print(f"  balance: correct=A {frac_A:.3f} (~0.5 expected by construction)")
    # One example prompt per base_case present (shows the different question framings).
    seen: set[str] = set()
    for t in trials:
        if t.base_case in seen:
            continue
        seen.add(t.base_case)
        _bc = "case7" if t.foil_type == PERSONA_VS_MODEL else t.base_case
        spec = resolve_spec(_bc, wording_version=opts.prompt_wording_version)
        text1 = _text(texts, t.text1_model, t.text1_persona, t.group, t.task_id)
        text2 = _text(texts, t.text2_model, t.text2_persona, t.group, t.task_id)
        prompt = build_prompt(t, text1, text2, spec, personas)
        print(f"\n  [{t.base_case}] example: eval=({model_slug(t.evaluator_model)},{t.evaluator_persona}) "
              f"foil=({model_slug(t.foil_model)},{t.foil_persona}) cond={t.condition} correct={t.correct_answer}")
        print("    " + (prompt[:650] + " …").replace("\n", "\n    "))
    print("=" * 72 + "\n")


def _spawn_workers(config_name: str, overrides, num_gpus: int, run_dir: Path,
                   run_id: str, model_index: int) -> None:
    procs = []
    for i in range(num_gpus):
        env = dict(os.environ, CUDA_VISIBLE_DEVICES=str(i))
        cmd = [sys.executable, "-m", "experiments.self_recognition.evaluate_cross_model",
               config_name, "--shard-index", str(i), "--num-shards", str(num_gpus),
               "--run-dir", str(run_dir), "--run-id", run_id,
               "--model-index", str(model_index)]
        for ov in overrides or []:
            cmd += ["--override", ov]
        logger.info(f"spawning GPU {i}: {' '.join(cmd)}")
        procs.append(subprocess.Popen(cmd, env=env))
    failed = sum(1 for p in procs if p.wait() != 0)
    if failed:
        raise SystemExit(f"{failed}/{num_gpus} cross-model workers failed")


def _run_shard(run_config: RunConfig, personas: dict[str, PersonaConfig],
               opts: CrossModelOptions, eval_model: str, run_dir: Path, run_id: str,
               shard_index: int, num_shards: int) -> None:
    from core.backends.hf_backend import HFBackend

    lay = layout_for(run_config.task, eval_model, opts.eval_dir)
    all_trials = read_trial_manifest(lay.trial_manifest)
    my = all_trials[shard_index::num_shards]
    if not my:
        logger.info(f"shard {shard_index}: no trials")
        return
    out_path = lay.deliverable if num_shards == 1 else (lay.shards_dir / f"eval_{shard_index}.jsonl")
    out_path.parent.mkdir(parents=True, exist_ok=True)
    done = load_done_trial_ids(lay.deliverable)
    rl = ResultsLogger(out_path)

    texts = load_all_texts(opts)
    gen_run_id = Path(opts.generation_sources[eval_model]).name
    logger.info(f"shard {shard_index}/{num_shards}: loading {eval_model} for "
                f"{len(my)} trials ({len(done)} done)")
    backend = HFBackend(eval_model, torch_dtype=resolve_dtype(run_config.model_dtype),
                        adapter=run_config.adapter)
    n = _run_trials(backend, my, personas, texts, run_id, gen_run_id, rl, opts, done, eval_model)
    logger.info(f"shard {shard_index}: wrote {n} rows")


def _run_for_model(config_name: str, run_config: RunConfig, personas: dict[str, PersonaConfig],
                   opts: CrossModelOptions, overrides, eval_model: str, model_index: int,
                   texts: dict) -> None:
    lay = layout_for(run_config.task, eval_model, opts.eval_dir)
    lay.run_dir.mkdir(parents=True, exist_ok=True)
    run_id = datetime.now().strftime("%Y%m%d_%H%M%S")
    trials = enumerate_trials(opts, texts, eval_model)
    if not trials:
        logger.warning(f"cross_model: no trials for evaluator {eval_model}; skipping")
        return
    write_trial_manifest(lay.trial_manifest, trials)
    logger.info(f"cross_model: evaluator={eval_model} → {len(trials)} trials → {lay.deliverable}")

    if opts.dry_run:
        _dry_run_report(opts, texts, eval_model, trials, personas)
        write_eval_manifest(lay.eval_manifest, opts, eval_model, trials, run_id)
        return

    num_gpus = resolve_num_gpus(run_config)
    if num_gpus > 1:
        if lay.shards_dir.exists():
            for f in lay.shards_dir.glob("eval_*.jsonl"):
                f.unlink()
        _spawn_workers(config_name, overrides or [], num_gpus, lay.run_dir, run_id, model_index)
        append_shard_outputs(lay.shards_dir, lay.deliverable, num_gpus)
        # The per-GPU shard files are intermediate: their rows are now merged into
        # the deliverable, and resume reads the DELIVERABLE (not the shards). Remove
        # them so the results dir holds only cross_model.jsonl (+ manifests). The
        # trial_manifest is kept for provenance.
        import shutil
        shutil.rmtree(lay.shards_dir, ignore_errors=True)
    else:
        _run_shard(run_config, personas, opts, eval_model, lay.run_dir, run_id,
                   shard_index=0, num_shards=1)
    write_eval_manifest(lay.eval_manifest, opts, eval_model, trials, run_id)
    logger.info(f"cross_model: evaluator={eval_model} complete → {lay.deliverable}")


def run(config_name: str, overrides: list[str] | None = None,
        shard_index: int | None = None, num_shards: int | None = None,
        run_dir: str | None = None, run_id: str | None = None,
        model_index: int | None = None) -> None:
    from run import apply_overrides, build_run_config, discover_configs, load_personas_for
    configs = discover_configs()
    if config_name not in configs:
        raise ValueError(f"Config '{config_name}' not found.")
    experiment_name, exp_config = configs[config_name]
    if overrides:
        exp_config = apply_overrides(exp_config, overrides)
    opts = _build_options(exp_config)
    # RunConfig gives us dtype/adapter/task/num_gpus; model_name is set per evaluator.
    run_config = build_run_config(experiment_name, {**exp_config,
                                                    "model_name": opts.eval_models[0],
                                                    "personas": list(opts.personas)})
    all_personas = load_personas_for(experiment_name)
    missing = set(opts.personas) - set(all_personas)
    if missing:
        raise ValueError(f"personas not in {experiment_name} registry: {sorted(missing)}")

    # Worker mode: run only the assigned evaluator model's shard.
    if shard_index is not None and num_shards is not None:
        eval_model = opts.eval_models[model_index]
        _run_shard(run_config, all_personas, opts, eval_model, Path(run_dir), run_id,
                   shard_index, num_shards)
        return

    # Launcher: load all texts once, then run each evaluator model sequentially.
    texts = load_all_texts(opts)
    for mi, eval_model in enumerate(opts.eval_models):
        _run_for_model(config_name, run_config, all_personas, opts, overrides,
                       eval_model, mi, texts)
    logger.info("cross_model: all evaluator models complete")


def main():
    logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")
    ap = argparse.ArgumentParser(description="Cross-model self-recognition eval.")
    ap.add_argument("config")
    ap.add_argument("--override", action="append", default=[])
    ap.add_argument("--shard-index", type=int, help=argparse.SUPPRESS)
    ap.add_argument("--num-shards", type=int, help=argparse.SUPPRESS)
    ap.add_argument("--run-dir", help=argparse.SUPPRESS)
    ap.add_argument("--run-id", help=argparse.SUPPRESS)
    ap.add_argument("--model-index", type=int, help=argparse.SUPPRESS)
    args = ap.parse_args()
    run(args.config, overrides=args.override, shard_index=args.shard_index,
        num_shards=args.num_shards, run_dir=args.run_dir, run_id=args.run_id,
        model_index=args.model_index)


if __name__ == "__main__":
    main()
