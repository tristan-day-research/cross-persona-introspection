@dataclass
class Layout:
    run_dir: Path
    deliverable: Path
    work: Path
    trial_manifest: Path
    shards_dir: Path
    eval_manifest: Path


def layout_for(task: str, eval_model: str, eval_dir: str) -> Layout:
    rd = TEXT_EVALUATIONS_DIR / task / model_slug(eval_model) / eval_dir
    work = rd / "_work"
    return Layout(
        run_dir=rd, deliverable=rd / "cross_model.jsonl", work=work,
        trial_manifest=work / "trial_manifest.jsonl", shards_dir=work / "_shards",
        eval_manifest=rd / "manifests" / "cross_model.eval_manifest.json",
    )


def write_trial_manifest(path: Path, trials: list[CrossModelTrial]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w") as f:
        for t in trials:
            f.write(json.dumps(asdict(t)) + "\n")


def read_trial_manifest(path: Path) -> list[CrossModelTrial]:
    out: list[CrossModelTrial] = []
    with open(path) as f:
        for line in f:
            line = line.strip()
            if line:
                out.append(CrossModelTrial(**json.loads(line)))
    return out


def write_eval_manifest(path: Path, opts: CrossModelOptions, eval_model: str,
                        trials: list[CrossModelTrial], run_id: str) -> None:
    from collections import Counter
    counts = Counter(f"{t.foil_type}|{t.condition}" for t in trials)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps({
        "experiment": "self_recognition_cross_model_eval",
        "run_id": run_id,
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "git_commit": git_commit(),
        "evaluator_model": eval_model,
        "eval_models": list(opts.eval_models),
        "generation_sources": opts.generation_sources,
        "groups": list(opts.groups),
        "personas": list(opts.personas),
        "foil_types": list(opts.foil_types),
        "conditions": list(opts.conditions),
        "max_pairs_per_cell": opts.max_pairs_per_cell,
        "sampling_seed": opts.sampling_seed,
        "n_trials_total": len(trials),
        "n_trials_by_foiltype_condition": dict(sorted(counts.items())),
    }, indent=2))


